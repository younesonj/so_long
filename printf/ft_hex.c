#include "ft_printf.h"

int	ft_hex(unsigned long n)
{
	int		i;
	char	*hex;

	i = 0;
	hex = "0123456789abcdef";
	if (n < 16)
		i += write(1, hex + n, 1);
	else if (n >= 16)
	{
		i += ft_hex(n / 16);
		i += ft_hex(n % 16);
	}
	return (i);
}
